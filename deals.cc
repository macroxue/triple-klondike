vector<Card> GetDeal(int seed) {
  const char* deal = deals[seed - 1];
  vector<Card> init_cards;
  for (int i = 0; i < kTotalCards; ++i) {
    int rank = ACE;
    switch (deal[i * 3]) {
      case 'A':
        rank = ACE;
        break;
      case '2':
      case '3':
      case '4':
      case '5':
      case '6':
      case '7':
      case '8':
      case '9':
        rank = deal[i * 3] - '1';
        break;
      case 'T':
        rank = R10;
        break;
      case 'J':
        rank = RJ;
        break;
      case 'Q':
        rank = RQ;
        break;
      case 'K':
        rank = KING;
        break;
      default:
        assert(!"bad rank");
    }
    int suit = SPADE;
    switch (deal[i * 3 + 1]) {
      case 'S':
        suit = SPADE;
        break;
      case 'H':
        suit = HEART;
        break;
      case 'D':
        suit = DIAMOND;
        break;
      case 'C':
        suit = CLUB;
        break;
      default:
        assert(!"bad suit");
    }
    init_cards.push_back(Card(suit, rank));
  }

  vector<Card> cards(&init_cards[kTotalCards - kInitStockCards],
                     &init_cards[kTotalCards]);
  cards.insert(cards.end(), &init_cards[0],
               &init_cards[kTotalCards - kInitStockCards]);
  return cards;
}

